        const message = this.querySelector('textarea').value;
        
        // Create mailto link
        const mailtoLink = `mailto:ashusrivstv063@gmail.com?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(`Name: ${name}\nEmail: ${email}\n\nMessage:\n${message}`)}`;
        
        // Open default email client
        window.location.href = mailtoLink;
        
